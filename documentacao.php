<?php

/* 
 * Função Somar
 * 
 * Esta função vai pegar o valor X e somar com o valor Y
 * 
 * @package Controle de Estoque
 * @author Orlando Nascimento
 * 
 * @parametro X float é o primeiro valor a ser somado
 * @parametro Y float é o segundo valor e ser somado
 * @retorna um valor um float(decimal)
 */

function Somar($x, $y)
{
    return ($x + $y);
}

